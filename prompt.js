/**
 * 构造用于视觉大模型解析文档图片的提示词
 * @param {Object} options
 * @param {string} [options.scene] - 场景，例如：RAG、OCR回填、知识库构建
 * @param {boolean} [options.keepOriginalText=true] - 是否保留图片中的原始文字
 * @param {boolean} [options.outputMarkdown=true] - 是否要求输出 markdown
 * @returns {string}
 */
export function buildVisionPrompt(options = {}) {
  const {
    scene = 'RAG知识库图片回填',
    keepOriginalText = true,
    outputMarkdown = true
  } = options
  // console.log("hintType", hintType);
  const commonRules = `
你是一个文档图片内容解析助手。

你的任务是识别并完整描述图片中的内容，目标是：
1. 让无法看到图片的人也能理解图片表达的信息
2. 让后续 RAG 系统能够检索到图片中的关键内容
3. 让图片翻译后的内容可以回填到原图位置附近

当前场景：${scene}

请遵循以下规则：

1. 先识别图片类型，例如：
- 流程图
- 方框图
- 系统架构图
- 网络拓扑图
- 柱状图
- 折线图
- 饼图
- 散点图
- 雷达图
- 表格
- 时序图
- UI界面图
- 地图
- 组织结构图
- 混合图文

2. 尽量提取图片中的所有文字，包括：
- 标题
- 副标题
- 图例
- 坐标轴名称
- 方框内文字
- 连线文字
- 注释
- 页脚备注
- 单位
- 时间
- 百分比
- 数值
${keepOriginalText ? '- 保留原始文字内容，不要只做意译' : ''}

3. 不要只做一句话总结，要按照图片结构描述：
- 图片由哪些部分组成
- 每部分之间是什么关系
- 信息流向是什么
- 哪些元素位于上方、下方、左侧、右侧、中间
- 哪些元素被箭头、线条、颜色、边框连接
- 哪些元素属于同一组
- 哪些内容最重要

4. 如果图片中有模糊、缺失、无法识别的内容，请明确写出“无法识别”。

5. 不要省略图片中的关键数字、百分比、时间、趋势、模块名称、节点名称。
`

  const flowChartRules = `
如果图片是流程图、业务图、方框图，请额外输出：

[流程步骤]
1. ...
2. ...
3. ...

[流程关系]
- A -> B
- B -> C
- 如果满足条件X，则进入 D
- 如果不满足，则进入 E

[补充说明]
- 箭头方向
- 条件分支
- 是否有循环
- 不同颜色或形状的含义
- 开始节点和结束节点

方框图的[详细描述]的内容，必须要体现出图里所有出现的数字。
`

  const chartRules = `
如果图片是统计图，请额外输出：

[图表类型]
例如：柱状图、折线图、饼图、散点图、雷达图

[坐标轴]
- 横轴表示什么
- 纵轴表示什么
- 数据单位是什么

[数据系列]
- 每种颜色分别表示什么
- 图例内容是什么
- 每组数据的含义

[关键趋势]
- 最大值
- 最小值
- 上升趋势
- 下降趋势
- 波动趋势
- 异常值
- 峰值或谷值

[趋势解释]
不仅描述数值，还要解释趋势变化，例如：
“2021年至2023年销售额持续增长，其中2022年至2023年增长最快。”
`

  const architectureRules = `
如果图片是系统架构图、模块图、网络拓扑图，请额外输出：

[模块列表]
- 模块A：作用是什么
- 模块B：作用是什么
- 模块C：作用是什么

[连接关系]
- 模块A 调用 模块B
- 模块B 读取 数据库C
- 用户请求先经过 网关D，再进入 服务E

[数据流]
- 数据从哪里来
- 数据流向哪里
- 哪些是上游系统
- 哪些是下游系统
`
const tableRules = `
如果图片是表格，请重点提取表格结构和每个单元格内容，而不仅仅是总结。

请按照以下方式描述：

[表格类型]
- 数据表
- 对比表
- 财务表
- 人员名单
- 时间计划表
- 配置表
- 参数表
- 统计表
- 混合表格

[表格标题]
- 提取表格标题
- 提取副标题
- 提取表头说明
- 提取单位、日期、备注

[表格结构]
- 一共有多少行、多少列
- 每列的字段名称是什么
- 是否存在多级表头
- 是否存在合并单元格
- 是否存在分组区域
- 是否存在小计、合计、汇总行
- 是否存在脚注、备注

[字段说明]
请按列说明字段含义，例如：
- 第一列：日期
- 第二列：产品名称
- 第三列：销售额
- 第四列：同比增长率

[表格数据]
请尽量逐行提取关键数据。

输出格式建议：

| 行号 | 字段1 | 字段2 | 字段3 |
| ---- | ---- | ---- | ---- |
| 1 | ... | ... | ... |
| 2 | ... | ... | ... |

如果表格过大，可以：
- 优先提取前几行示例
- 提取关键行
- 提取最大值、最小值、异常值、汇总值
- 总结主要规律

[表格规律]
除了逐行提取，还需要总结：
- 哪些字段增长或下降
- 哪些字段差异最大
- 是否存在明显异常值
- 哪些行最重要
- 是否存在排名、趋势、分类关系

[特殊情况]
- 如果表格中包含空白单元格，请明确说明为空
- 如果存在无法识别内容，请标记“N/A”
- 如果存在合并单元格，请说明该单元格影响的行列范围
- 如果表格中混有图片、图标、颜色标识，也请补充说明其含义
`


  const outputTemplate = `
最终请使用如下格式输出（必须保留分段标题）：

[图片摘要]
（1~3句，面向检索与召回，尽量包含主题词）

[图片详细描述]
（可较长，包含结构、对象关系、关键文本、关键数字、趋势、单位）

[图片类型]
...

[图片标题]
...

[图片主要内容]
...

[图片中的关键文本]
- ...
- ...
- ...

[结构关系]
...

[补充说明]
...

[结构化输出(JSON)]
{"summary":"...","detail":"...","title":"...","type":"...","keywords":["..."],"numbers":["..."]}
`
const photoRules = `
如果图片是一张普通照片、场景图、人物图、实物图，请不要像流程图或统计图一样过度结构化，而是使用简洁自然的语言描述。

请重点描述：

[照片类型]
- 人物照片
- 风景照片
- 室内照片
- 街景照片
- 商品照片
- 截图
- 混合场景

[主体内容]
- 图片里主要有什么
- 人物、物体、建筑、动物分别是什么
- 谁位于画面中央
- 哪些元素最显眼
- 是否存在多个主体

[场景信息]
- 场景发生在室内还是室外
- 大概是什么地点
- 是白天、夜晚、黄昏还是阴天
- 是否有明显天气特征
- 场景氛围如何

[位置关系]
- 主体位于左侧、右侧、中间、前景、背景
- 人物或物体之间有什么相对位置关系
- 是否存在动作、互动、朝向

[文字内容]
如果照片中包含招牌、屏幕、海报、标签、字幕、路牌等文字，也请提取其中的重要文字。

[简要总结]
最后用1到3句话总结这张照片主要表达的内容，不要过长，不要过度推测图片背后的故事。
`

const textImageRules = `
如果图片中的内容以文字为主（例如：文档截图、论文片段、报告段落、说明书、截图文本等），请优先执行“文本还原”，而不是图像描述。

目标：将图片尽可能还原为结构清晰、可用于RAG检索的文本内容。

请遵循以下规则：

[文本还原优先]
- 优先逐行提取图片中的所有文字内容
- 尽量保持原有语义，不要随意改写
- 不要只做总结，必须尽量完整还原文本

[结构还原]
根据视觉排版恢复结构：
- 标题（大标题、小标题）
- 段落
- 列表（有序 / 无序）
- 编号（1. / (1) / 一、等）
- 分点说明
- 引用内容
- 注释 / 脚注

[格式增强（用于RAG）]
在不改变原意的前提下，可以适度增强结构：
- 使用 markdown 标题（#、##、###）
- 使用列表（- / 1.）
- 保留段落分隔
- 保持逻辑层级

[关键元素提取]
特别注意提取以下内容：
- 专业术语
- 指标名称
- 关键结论
- 定义类句子（例如“XXX是指...”）
- 数值、百分比、时间
- 条件语句（如果…则…）
- 因果关系（因为…所以…）

[表格 / 列表嵌入]
如果文本中包含类似表格或列表：
- 尽量转成 markdown 表格或列表
- 保留字段名称与对应关系

[语言处理]
- 如果图片是非目标语言（例如英文），请：
  1. 保留原文
  2. 提供对应翻译（中英文对照更佳）
- 不要丢失原始关键词（对检索很重要）

[无法识别]
- 对无法识别的部分标注：“[无法识别]”

[输出要求]
输出应尽量接近“可阅读的文档内容”，而不是“图片说明”。

避免输出：
❌ “图片展示了一段文字……”
❌ “这是一段关于XXX的说明……”

正确输出：
✅ 直接还原文本内容 + 结构
`

  return [
    commonRules,
    flowChartRules,
    chartRules,
    architectureRules,
    photoRules,
    tableRules,
    textImageRules,
    outputTemplate,
    outputMarkdown
      ? '\n请使用 markdown 输出，合理使用标题、列表、缩进。'
      : ''
  ]
    .filter(Boolean)
    .join('\n')
}

